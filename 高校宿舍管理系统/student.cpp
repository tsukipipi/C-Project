/////////////////////////////////////////////
//show函数
//函数功能：展示学生信息
//函数参数：无
//参数返回值：无
//
void Student::show()
{
    cout<<"\n姓名："<<name<<endl;
    cout<<"性别：";
    if(sex=='f')cout<<"女"<<endl;
    else cout<<"男"<<endl;
    cout<<"学号："<<number<<endl;
    cout<<"电话号码："<<phone<<endl;
    cout<<"宿舍号："<<roomNumber<<endl;
    cout<<"床位号："<<bedNumber<<endl;
    cout<<"入宿状态：";
    if(mark==0)cout<<"尚未入宿"<<endl;
    else cout<<"已入宿"<<endl;
}


/////////////////////////////////////////////
//set函数
//函数功能：设置宿舍号和床位号
//函数参数：整型，宿舍号room和床位号bed
