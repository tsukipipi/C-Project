/////////////////////////////////////////////
//添加函数
//函数功能：加入新学生，登记信息，将删除标记记为1，表示未删
//函数参数：字符型指针、字符型，整型。分别代表学生各类信息
//参数返回值：无
//
void Student::addstudent(char *na,char s,int num,char *ph)
{
    strcpy(name,na);
    sex=s;
    number=num;
    strcpy(phone,ph);
    note=1;             //删除标记（0：已删 1：未删）
}


